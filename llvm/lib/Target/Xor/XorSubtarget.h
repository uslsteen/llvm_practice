#ifndef __LLVM_LIB_TARGET_SIM_SIMSUBTARGET_H__
#define __LLVM_LIB_TARGET_SIM_SIMSUBTARGET_H__

#include "MCTargetDesc/XorInfo.h"
#include "XorFrameLowering.h"
#include "XorISelLowering.h"
#include "XorInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "XorGenSubtargetInfo.inc"

namespace llvm {

class StringRef;
class TargetMachine;

class XorSubtarget : public XorGenSubtargetInfo {
  virtual void anchor();

  XorRegisterInfo RegInfo;
  XorInstrInfo InstrInfo;
  XorFrameLowering FrameLowering;
  XorTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;
  XorABI::ABI TargetABI = XorABI::ABI_ILP32;
  MVT XLenVT = MVT::i32;
  unsigned XLen = 32;

public:
  XorSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
               const TargetMachine &TM);

  /// Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const XorInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const XorFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const XorTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const XorRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  auto getTargetABI() const { return TargetABI; }
  auto getXLenVT() const { return XLenVT; }
  auto getXLen() const { return XLen; }
};

} // end namespace llvm

#endif // __LLVM_LIB_TARGET_SIM_SIMSUBTARGET_H__